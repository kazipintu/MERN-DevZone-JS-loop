/*  ~ for loop, while loop, do while loop ~  */

/* FOR LOOP */
for (let i = 1; i <= 10; i++) {
  console.log("MERN");
}

// infinite loop will run without any condition.

/* WHILE LOOP */
{
  let i = 1;
  while (i <= 5) {
    console.log("i = ", i);
    i++;
  }
}

/* DO WHILE LOOP */

{
  let i = 1;
  do {
    console.log("i = ", i);
    i++;
  } while (i <= 10);
}